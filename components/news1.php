                            <p>Peserta didik SMAN 2 Probolinggo kembali mempersembahkan juara tingkat internasional. Kabar membanggakan datang dari ajang GloCoLis (Global Competition for Life Sciences) 2022 yang diselenggarakan Indonesian Young Scientist Association (IYSA) yang berkolaborasi dengan Food Science and Technology Department, Bogor Agricultural University (IPB) and BUCA IMSEF Turkey dan diikuti oleh 130 tim dari 11 negara. Dua tim asal Sekolah Menengah Atas Negeri 2 Probolinggo berhasil meraih medali emas dan perak dalam kejuaraan karya tulis ilmiah tingkat internasional yang dilaksanakan secara daring atau online pada 9 – 12 April 2022.</p>

                            <p>IYSA bekerjasama dengan Departemen Ilmu dan Teknologi Pangan, Institut Pertanian Bogor (IPB) dan BUCA IMSEF Turki mengadakan kompetisi invensi tingkat internasional khusus untuk bidang Life Sciences yang disebut “Global Competition for Life Sciences (GloCoLis)” sebagai platform pembelajaran yang tepat untuk membangun daya saing global dengan kompetensi ilmiah dan membangun jaringan peneliti yang kuat dan profesional industri yang mampu memenuhi standar nasional dan internasional. Kegiatan tersebut dilakukan dengan tujuan yaitu melakukan inovasi penelitian dan pengembangan di bidang ilmu hayati pada siswa internasional, untuk mengembangkan inovasi berkualitas tinggi dalam ilmu kehidupan untuk meningkatkan kualitas hidup negara, dan membangun kerjasama yang berkelanjutan dengan siswa internasional lainnya.</p>

                            <p>Pada ajang ini SMAN 2 Probolinggo mengirimkan 2 tim terbaiknya.</p>

                            <p>A. Tim 1 GloCoLis</p>
                            <p>Tim 1 GloCoLis beranggotakan yaitu Emilio Dwiky Cesar Padilla, Abimanyu Yudistya Prayuda, Fadhil Rizki Himawan, Laura Ayu Fitria, dan Adinda Aulia Magdis Varidianty yang telah meraih Medali Emas (GOLD MEDAL) pada kategori kelas perlombaan bidang medicine jenjang secondary school dengan judul karya: RAPID DECOLISH (Smart Detector Cobalt from Lingula unguis) Based on Physical Morphological Characterization of Inarticulate Shells Powder.</p>

                            <p>B. Tim 2 GloCoLis</p>
                            <p>Tim 2 GloCoLis beranggotakan yaitu Muhammad Yardan Maulana Sukoco, Eka Robiatul Adawiyah, Queency Vierza Anora, Dina Rosyida Kusuma Putri, dan Sela Halimatus Sakdiah juga telah meraih Medali Perak ( SILVER MEDAL) pada kategori kelas perlombaan bidang biotechnology jenjang secondary school dengan judul karya: Physical Characterization of Biodiesel Quality from Used Cooking Oil Based on a Treatment With Technology from ADSOTERA and Ultrasonic Vibrating Bath.</p>

                            <p>Prestasi ini membuat Kepala SMAN 2 Probolinggo, Ibu Dra. Erni Prasetyawati, M.Pd. sangat bangga dan mengapresiasi hasil kerja keras dan cerdas peserta didik berprestasi tersebut. Beliau berpesan agar terus melanjutkan bakatnya walau nanti sudah lulus dari SMAN 2 Probolinggo.</p>
